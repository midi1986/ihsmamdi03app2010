@implementation RootViewController

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}


- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [appDelegate.mensas count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithFrame:CGRectZero reuseIdentifier:CellIdentifier] autorelease];
    }
	
	Mensa *aMensa = [appDelegate.mensas objectAtIndex:indexPath.row];
	
	cell.text = aMensa.tag;
	cell.accessoryType = UITableViewCellAccessoryDisclosureIndicator;
    
    // Set up the cell
    return cell;
}


- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    // Navigation logic -- create and push a new view controller
	
	if(bdvController == nil)
		bdvController = [[MensaDetailViewController alloc] initWithNibName:@"MensaDetailView" bundle:[NSBundle mainBundle]];
	
	Mensa *aMensa = [appDelegate.mensas objectAtIndex:indexPath.row];
	
	bdvController.aMensa = aMensa;
	
	[self.navigationController pushViewController:bdvController animated:YES];
}



- (void)viewDidLoad {
    [super viewDidLoad];
    // Uncomment the following line to add the Edit button to the navigation bar.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
	
	appDelegate = (MensaAppDelegate *)[[UIApplication sharedApplication] delegate];
	
	self.title = @"Mensas";
}



